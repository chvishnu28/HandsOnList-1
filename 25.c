#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>
#include <sys/wait.h>

int main() {
    int pid1, pid2, pid3;
    pid1 = fork();
    if (pid1 == 0) {
        printf("Child 1, PID = %d\n", getpid());
        exit(0);
    }

    pid2 = fork();
    if (pid2 == 0) {
        printf("Child 2, PID = %d\n", getpid());
        exit(0);
    }

    pid3 = fork();
    if (pid3 == 0) {
        printf("Child 3, PID = %d\n", getpid());
        exit(0);
    }

    waitpid(pid2, NULL, 0);
    printf("Parent waited for Child 2\n");

    return 0;
}
