#include<stdio.h>
#include<unistd.h>
#include<stdlib.h>

int main(){

	int pid;
	pid=fork();

	if(!pid)
	{
		printf("child Process Pid: %d,parent Pid = %d\n",getpid(),getppid());
	}
	else
	{
		printf("Parent Pid:%d,child pid:%d",getpid(),pid);

	}
	return 0;
}
