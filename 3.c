#include<stdio.h>
#include<unistd.h>
#include<fcntl.h>

int main()
{
	int fd;
	fd=creat("file3rdqn.txt",0666);
	if(fd==-1)
	{
		perror("creat");
		return 1;
	}

	printf("File created Successfully : File Descriptor Value= %d",fd);
	close(fd);
	return 0;

}
