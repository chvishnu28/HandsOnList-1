#include<stdio.h>
#include<unistd.h>
#include<stdlib.h>
#include<fcntl.h>
#include<string.h>

int main()
{
	int fd,pid;
	fd=open("Output.txt",O_RDWR|O_CREAT|O_TRUNC, 0644);
        pid=fork();
       if(!pid)
       {
          char msg[]="Hello from child\n";
	  write(fd,msg,strlen(msg));
	  printf("Child pid =%d ",getpid());
       }	       
       else{
	       char msg[]="Hello from parent\n";
	       write(fd,msg,strlen(msg));
	       printf("Parent pid = %d ",getpid());
       }
       close(fd);
       return 0;

}
