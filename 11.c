#include <stdio.h>
#include <unistd.h>
#include <fcntl.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
    int fd, fd_new;

    if (argc != 2) {
        write(2, "Usage: ./a.out <filename>\n", 27);
        exit(1);
    }

    fd = open(argv[1], O_WRONLY | O_CREAT | O_APPEND, 0644);
   

    fd_new = fcntl(fd, F_DUPFD, 50);  // duplicate into fd >= 50

    write(fd, "Hello from fd\n", 14);
    write(fd_new, "Hello from fd_new\n", 18);

    close(fd);
    close(fd_new);
    return 0;
}




