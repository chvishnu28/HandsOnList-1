#include<stdio.h>
#include<unistd.h>
#include<sys/resource.h>

int main()
{
	int prio;
	prio=getpriority(PRIO_PROCESS,0);

	printf("Process ID: %d\n",getpid());
	printf("Current priority: %d\n",prio);

	return 0;
}

