#include<stdio.h>
#include<fcntl.h>
#include<unistd.h>

int main()
{
	int fd;
	fd=open("My4txt.txt",O_CREAT | O_EXCL |O_RDWR,0666);

	if(fd==-1)
	{
		perror("open");
		return 1;
	}
	printf("file opened successfullt FD = %d\n",fd);
	close(fd);
	return 0;



}

