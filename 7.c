#include <fcntl.h>
#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <stdio.h>

int main(int argc, char *argv[]) {
    int fd1, fd2;
    char size[1024];
    int n;

    if (argc != 3) {
        write(2, "Error <file1> <file2>\n",
              strlen("Error <file1> <file2>\n"));
        exit(1);
    }

    fd1 = open(argv[1], O_RDONLY);

    fd2 = open(argv[2], O_WRONLY | O_CREAT | O_TRUNC, 0644);

    n = read(fd1, size, sizeof(size));
    if (n > 0) {
        write(fd2, size, n);
    }

    close(fd1);
    close(fd2);

    return 0;   
}
